package it.tecnosphera.booking.classroom.model;

public class User {
	private long id;
	private String name;
	
	public long getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public void setId(long id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
		
}
